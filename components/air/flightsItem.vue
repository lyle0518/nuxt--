<script>
export default {
  //   props: ["data"],
  props: {
    data: {
      type: Object,
      default: {}
    }
  },
  data() {
    return {
      isShow: false
    };
  },
  computed: {
    //   飞行时间
    // 涉及一些计算可以分摊到前端浏览器进行运算,节省后端服务器的压力
    rankTime() {
      //到达时间
      const arr = this.data.arr_time.split(":");
      //起飞时间
      const dep = this.data.dep_time.split(":");
      console.log(arr, dep);
      //   飞行相隔时间,arr[0]乘法有隐式转换成数字,arr[1]仍是字符串

      let dis = arr[0] * 60 + +arr[1] - (dep[0] * 60 + +dep[1]);
      if (dis < 0) {
        // dis = dis + 24 * 60;
        dis = dis + 24 * 60;
      }
      console.log(dis);
      //   转化成时间的格式
      const hours = Math.floor(dis / 60);
      const min = dis % 60;
      return `${hours}时${min}分`;
    }
  },
  mounted() {
    // console.log(this.data);

    console.log(this.rankTime);
  }
};
</script>

